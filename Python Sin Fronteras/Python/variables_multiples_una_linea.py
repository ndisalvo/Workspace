#Como podemos definir multiples variables en una sola linea:
variable1, variable2, variable3 = 'Nico', 'Luli', 'Mimus'
print(variable1)
print(variable2)
print(variable3)

#Para definir multiples variables las cuales tengan el mismo valor:
var1 = var2 = var3 = 'Abemus Mimus'
print(var1)
print(var2)
print(var3)